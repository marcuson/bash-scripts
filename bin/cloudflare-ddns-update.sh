Mbs:Io:confirm() {
	local default_choice text_suffix
	default_choice="$1"
	[[ $default_choice =~ ^[Yy]$ ]] && text_suffix="[Y/n]" || text_suffix="[y/N]"

	read -r -p "$2 $text_suffix " -n 1
	echo " "

	local answer
	answer="${REPLY:-$default_choice}"
	[[ $answer =~ ^[Yy]$ ]]
}

